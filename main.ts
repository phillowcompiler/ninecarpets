controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    if (posx >= 5 && posx <= 7 && posy >= 2 && posy <= 4) {
        index22 = (posy - 2) * 3 + (posx - 5)
        if (0 <= flgChangeTile) {
            if (flgChangeTile != index22) {
                tmptile = lstPlayTiles[index22]
                lstPlayTiles[index22] = lstPlayTiles[flgChangeTile]
                lstPlayTiles[flgChangeTile] = tmptile
                mySprite.say("Change!", 500)
                dispTile(posx, posy, lstPlayTiles[index22])
                dispTile(flgChangeTile % 3 + 5, Math.floor(flgChangeTile / 3) + 2, tmptile)
                hilite.startEffect(effects.fire, 500)
                sprChangeFrom.startEffect(effects.fire, 500)
            }
            flgChangeTile = -1
            sprChangeFrom.setFlag(SpriteFlag.Invisible, true)
            chkGameWin()
        } else {
            flgChangeTile = index22
            mySprite.say("From", 500)
            sprChangeFrom.setPosition(posx * 16 + 8, posy * 16 + 8)
            sprChangeFrom.setFlag(SpriteFlag.Invisible, false)
        }
    }
})
function chkGameWin () {
    index3 = 0
    while (index3 >= 0 && index3 < 9) {
        tmptile = lstPlayTiles[index3] % 10
        if (tmptile == lstBaseTiles[index3] % 10) {
            index3 += 1
        } else {
            index3 = -1
        }
    }
    if (index3 >= 9) {
        for (let index = 0; index <= 8; index++) {
            dispTile(index % 3 + 5, Math.floor(index / 3) + 2, lstPlayTiles[index])
        }
        music.baDing.play()
        effects.smiles.startScreenEffect(2000)
        pause(2000)
        game.over(true, effects.confetti)
    }
}
function makeTiles () {
    tileList = [0, 1, 2, 3, 4, 5]
    lstTilePatternID = []
    while (lstTilePatternID.length < 6) {
        rnd = Math.randomRange(1, tileList.length) - 1
        lstTilePatternID.push(tileList[rnd])
        tmptile = tileList.removeAt(rnd)
    }
    initTile(true)
    initTile(false)
}
